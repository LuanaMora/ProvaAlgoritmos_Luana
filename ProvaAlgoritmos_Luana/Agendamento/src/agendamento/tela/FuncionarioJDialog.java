    private void salvar() {
        Funcionario funcionario = new Funcionario();
        funcionario.setCodigo(Integer.parseInt(tfCodigo.getText()));
        funcionario.setNomeFuncionario(tfNome.getText());
        funcionario.setNumeroMatricula(Integer.parseInt(tfMatricula.getText()));

        try {
            if (funcionarioDAO.getLastId() == Integer.parseInt(tfCodigo.getText())) {
                funcionarioDAO.save(funcionario);
                JOptionPane.showMessageDialog(null, "Registro Salvo Com Sucesso!!!");

            } else {
                funcionarioDAO.update(funcionario);
                JOptionPane.showMessageDialog(null, "Registro Atualizado Com Sucesso!!!");
            }

            carregaTable(funcionarioDAO.getAll());
            btSalvar.setText("Salvar");
            btSalvar.setEnabled(false);
            limparCampos();
        } catch (Exception ex) {
            ex.printStackTrace();
        }

    }

    private void limparCampos() {
        tfCodigo.setText("");
        tfNome.setText("");
        tfMatricula.setText("");
    }

    private void carregaTable(List<Funcionario> funcionarioList) {
        if (funcionarioList == null) {
            return;
        }
        DefaultTableModel model = (DefaultTableModel) tableFuncionario.getModel();
        model.setRowCount(0);
        for (Funcionario f : funcionarioList) {
            model.addRow(new Object[]{f.getCodigo(), f.getNomeFuncionario(), f.getNumeroMatricula()});
        }
    }

    private void setTfCodigo() {
        try {
            tfCodigo.setText(String.valueOf(funcionarioDAO.getLastId()));
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }

    private void remover() {
        int linhaSeleciona = tableFuncionario.getSelectedRow();
        if (linhaSeleciona == -1) {
            JOptionPane.showMessageDialog(null, "Selecione uma linha para Remover", "ATENÇÃO", JOptionPane.ERROR_MESSAGE);
            return;
        }
        int codigoRemover = (int) tableFuncionario.getValueAt(linhaSeleciona, 0);
        try {
            funcionarioDAO.delete(codigoRemover);
            limparCampos();
            btSalvar.setText("Salvar");
            btSalvar.setEnabled(false);
            btRemover.setEnabled(false);
            JOptionPane.showMessageDialog(null, "Removido com Sucesso");
            carregaTable(funcionarioDAO.getAll());
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }

    private void eventoTableAtualizar() {
        Funcionario funcionario = new Funcionario();
        try {
            funcionario = funcionarioDAO.getById((int) tableFuncionario.getValueAt(tableFuncionario.getSelectedRow(), 0));
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
        btSalvar.setText("Atualizar");
        tfCodigo.setText(String.valueOf(funcionario.getCodigo()));
        tfNome.setText(funcionario.getNomeFuncionario());
        tfMatricula.setText(String.valueOf(funcionario.getNumeroMatricula()));
        btSalvar.setEnabled(true);
        btRemover.setEnabled(true);
    }

