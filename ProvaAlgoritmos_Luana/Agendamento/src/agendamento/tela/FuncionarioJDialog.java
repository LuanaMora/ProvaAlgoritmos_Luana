    private void filtrar() {
        try {
            if (rbCodigo.isSelected() && tfCN.getText().trim().length() > 0) {//Codigo está selecionado
                Funcionario funcionario = funcionarioDAO.getById(Integer.parseInt(tfCN.getText()));
                List<Funcionario> funcionarioList = new ArrayList<>();
                funcionarioList.add(funcionario);
                carregaTable(funcionarioList);
            } else if (rbNome.isSelected() && tfCN.getText().trim().length() > 0) {
                carregaTable(funcionarioDAO.getByName(tfCN.getText()));
            } else {
                JOptionPane.showMessageDialog(null, "Favor Informe um filtro para Pesquisa...", "ATENÇÃO", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Ocorreu um erro inesperado!", "ERRO", JOptionPane.ERROR_MESSAGE);
        }
    }

