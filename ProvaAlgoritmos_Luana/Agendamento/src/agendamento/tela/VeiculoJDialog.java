    private void salvar() {
        Veiculo veiculo = new Veiculo();
        veiculo.setCodigoVeiculo(Integer.parseInt(tfCodigo.getText()));
        veiculo.setAnoVeiculo(Integer.parseInt(tfAno.getText()));
        veiculo.setNumeroPlaca(Integer.parseInt(tfPlaca.getText()));
        veiculo.setNumeroPassageiros(Integer.parseInt(tfPassageiros.getText()));

        try {
            if (veiculoDAO.getLastId() == Integer.parseInt(tfCodigo.getText())) {
                veiculoDAO.save(veiculo);
                JOptionPane.showMessageDialog(null, "Registro Salvo Com Sucesso!!!");

            } else {
                veiculoDAO.update(veiculo);
                JOptionPane.showMessageDialog(null, "Registro Atualizado Com Sucesso!!!");
            }

            carregaTable(veiculoDAO.getAll());
            btSalvar.setText("Salvar");
            btSalvar.setEnabled(false);
            limparCampos();
        } catch (Exception ex) {
            ex.printStackTrace();
        }

    }

    private void limparCampos() {
        tfCodigo.setText("");
        tfAno.setText("");
        tfPassageiros.setText("");
        tfPlaca.setText("");
    }

    private void carregaTable(List<Veiculo> veiculoList) {
        if (veiculoList == null) {
            return;
        }
        DefaultTableModel model = (DefaultTableModel) tableVeiculo.getModel();
        model.setRowCount(0);
        for (Veiculo v : veiculoList) {
            model.addRow(new Object[]{v.getCodigoVeiculo(), v.getNumeroPlaca(), v.getAnoVeiculo(), v.getNumeroPassageiros()});
        }
    }

    private void setTfCodigo() {
        try {
            tfCodigo.setText(String.valueOf(veiculoDAO.getLastId()));
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }

    private void remover() {
        int linhaSeleciona = tableVeiculo.getSelectedRow();
        if (linhaSeleciona == -1) {
            JOptionPane.showMessageDialog(null, "Selecione uma linha para Remover", "ATENÇÃO", JOptionPane.ERROR_MESSAGE);
            return;
        }
        int codigoRemover = (int) tableVeiculo.getValueAt(linhaSeleciona, 0);
        try {
            veiculoDAO.delete(codigoRemover);
            limparCampos();
            btSalvar.setText("Salvar");
            btSalvar.setEnabled(false);
            btRemover.setEnabled(false);
            JOptionPane.showMessageDialog(null, "Removido com Sucesso");
            carregaTable(veiculoDAO.getAll());
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }

    private void eventoTableAtualizar() {
        Veiculo veiculo = new Veiculo();
        try {
            veiculo = veiculoDAO.getById((int) tableVeiculo.getValueAt(tableVeiculo.getSelectedRow(), 0));
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
        btSalvar.setText("Atualizar");
        tfCodigo.setText(String.valueOf(veiculo.getCodigoVeiculo()));
        tfAno.setText(String.valueOf(veiculo.getAnoVeiculo()));
        tfPlaca.setText(String.valueOf(veiculo.getNumeroPlaca()));
        tfPassageiros.setText(String.valueOf(veiculo.getNumeroPassageiros()));
        btSalvar.setEnabled(true);
        btRemover.setEnabled(true);
    }

