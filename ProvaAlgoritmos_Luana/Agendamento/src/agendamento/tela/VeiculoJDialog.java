    private void filtrar() {
        try {
            if (rbCodigo.isSelected() && tfCA.getText().trim().length() > 0) {//Codigo está selecionado
                Veiculo veiculo = veiculoDAO.getById(Integer.parseInt(tfCA.getText()));
                List<Veiculo> veiculoList = new ArrayList<>();
                veiculoList.add(veiculo);
                carregaTable(veiculoList);
            } else if (rbAno.isSelected() && tfCA.getText().trim().length() > 0) {
                carregaTable(veiculoDAO.getByName(tfCA.getText()));
            } else {
                JOptionPane.showMessageDialog(null, "Favor Informe um filtro para Pesquisa...", "ATENÇÃO", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Ocorreu um erro inesperado!", "ERRO", JOptionPane.ERROR_MESSAGE);
        }
    }
